void main(){
// This program demonstrates understanding of Dart data types: int, double, String, List, and Map.

// Integer (int)
int age = 25; // Stores whole numbers
print("My age is $age years old.");

// Double (double)
double pi = 3.14159;
 // Stores floating-point numbers
print("The value of Pi is $pi.");

// String
String name = "John Doe"; // Stores sequences of characters
print("My name is $name.");

// List
List<String> fruits = ["apple", "banana", "orange"]; // Ordered collection of elements
print("My favorite fruits are: $fruits.");

// Map
Map<String, String> person = {
  "name": "Jane Doe",
  "age": "30",
  "city": "New York"
}; // Key-value pairs
print("Person details: $person.");
}

